// Fish Functions
export const getFishBySeason = (season, fish) => {
  if (Array.isArray(season)) {
    return fish.filter((fish) => {
      const combinedSeasons = new Set(fish.season.concat(season));
      return combinedSeasons.size != fish.season.length + season.length;
    });
  }
  return fish.filter((fish) => fish.season.includes(season));
};

